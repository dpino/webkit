function callTypeInformationTryCleanup() {
    {
        // Just an empty module
        new WebAssembly.Module(new Uint8Array([0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00]));
    }

    gc();
}

function createTag() {
    const instance = new WebAssembly.Instance(new WebAssembly.Module(WASM_CODE));

    return instance.exports.tag;
}

function main() {
    const tag = createTag();

    callTypeInformationTryCleanup();

    try {
        new WebAssembly.Exception(tag, [{}]);
        throw new Error("Should have thrown TypeError");
    } catch (e) {
        if (!(e instanceof TypeError) || e.message !== "Argument value did not match the reference type")
            throw e;
    }
}

main();
